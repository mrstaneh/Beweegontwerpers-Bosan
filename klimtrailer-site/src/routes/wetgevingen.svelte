      <h1>Onderbouwing van de valmat</h1>
      <p>
        Om te boulderen moet je aan een paar veiligheidseisen voldoen. Een eis
        hiervan is dat je de juiste dikte van een mat hanteert. Doe je dit niet,
        dan mag er niet geklommen worden, dit kan namelijk erg gevaarlijk zijn.
        Volgens de Europese normen (EN12572-2:2017) zijn dit de richtlijnen voor
        een valmat:
      </p>
      <ul>
        <li>
          De dikte van de valmat is minimaal 300 millimeter bij een maximale
          klimhoogte van 4,5 meter.
        </li>
        <li>
          Bij overhangende wanden mag tussen het wandvoet en de mat een gedeelte
          met dunne matten worden ingericht [<a
