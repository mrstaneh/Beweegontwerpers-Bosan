      <h1>Wetgevingen: Speel- en sporttoestellen</h1>
      <p>
        De Bouldermini zal gaan functioneren als een speel- en sporttoestel.
        Voor beide functies zijn andere wetgevingen, deze zullen in dit
        hoofdstuk aan bod komen. Allereerst zal het gaan over de wetten die van
        toepassing zijn op een speel- en sporttoestel. Daarna zullen de
        specifieke wetgevingen aan bod komen over de toestellen apart, maar ook
        over het gebruik van een aanhanger.
      </p>

      <p style="color: #F06119; display: inline;">De aanhanger</p>
      <p>
        Als de Bouldermini verplaatst wordt, is de wetgeving over het gebruik
        van een aanhanger van belang. Er zijn verschillende punten waar een
        aanhanger aan moet voldoen, zo zijn en er in de lengte, breedte en
        hoogte maximale afmetingen, maar speelt ook het gewicht een belangrijke
        rol. Daarnaast zijn er naast de standaard geldende snelheid regels nog
        extra snelheid regels bij het gebruik van een aanhanger. De
        specificaties van deze regels zijn te vinden in de <a
          href="/pdf-bijlage/wetgevingen.pdf"
          target="_blank">bijlage</a
        >, deze gaat over de wetgeving.
      </p>

      <p style="color: #F06119; display: inline;">Keuringen</p>
      <p>
        Als het toestel voor het eerst in gebruik genomen wordt, moet het
        toestel bij verschillende situaties gekeurd worden. Denk hierbij aan het
        checken van de installatie en nagaan of de Boulder mini veilig
        functioneert. Deze verschillende checks moeten plaats vinden als het
        toestel voor het eerst in gebruik genomen wordt, het toestel verplaatst
        wordt of zodra er een montage plaatsvindt.
      </p>

      <p style="color: #F06119; display: inline;">Het gymtoestel</p>
      <p>
        Naast de wetten die gelden voor een speel- en sporttoestel zijn er ook
        een aantal wetten die alleen gelden voor een gymtoestel. Denk hierbij
        aan een jaarlijkse inspectie die uitgevoerd moet worden. Verder zijn er
        ook wetten die ervoor zorgen dat de productie volgens de richtlijnen
        verloopt, zo moet er een CE-markering, fabrikantenverklaring en een
        Nederlandstalige gebruiksaanwijzing zijn.
      </p>

      <p style="color: #F06119; display: inline;">Het speeltoestel</p>
      <p>
        Het toestel zal niet alleen inzetbaar zijn tijdens de gymlessen, maar
        zal ook gebruikt kunnen worden als speeltoestel. Dit zal voornamelijk
        gebeuren in de pauzes. Echter zijn de regels voor speeltoestellen net
        iets anders dan voor een gymtoestel. Dit komt doordat er tijdens de
        pauze niet voldoende toezicht gehouden kan worden. Naast de eerder
        besproken regels moet er vooral rekening gehouden worden met de
        ondergrond en de valhoogte. Als er geen sprake is van toezicht mag
        namelijk maar tot een hoogte van 3 meter geklommen worden, mits er
        sprake is van de juiste bodem onder het speeltoestel. Dit is het
        grootste verschil als het gaat om de wetgeving tussen een sport- en
        speeltoestel. Er zullen dus aanpassingen gedaan moeten worden aan de
        Boulder mini zodat deze tijdens de pauze voldoet aan de wet. Deze
        aanpassing zal ervoor zorgen dat het niet mogelijk is om hoger te
        klimmen dan 3 meter, hierdoor voldoet de Boulder mini aan de eisen.
      </p>
      <p>
        Verder moet het speeltoestel gepositioneerd worden boven zand, grind,
        boomschors of houtsnippers. Al deze opties moeten minstens 300 mm dik
        zijn om te voldoen als ondergrond bij een valhoogte van 3 meter.
      </p>

      <p style="color: #F06119; display: inline;">Conclusie</p>
      <p>
        De Boulder mini zal dus gaan functioneren als een speel- en gymtoestel,
        voor beide zijn de regels voor het verplaatsen van het toestel
        hetzelfde. Een groot verschil is echter dat er bij een gymtoestel
        jaarlijks een inspectie moet plaatsvinden om de veiligheid te
        waarborgen.
      </p>
      <p>
        Daarnaast is er een verschil in de combinatie tussen de valhoogte en de
        ondergrond. Bij het gebruik als gymtoestel mag er tot 4 meter hoog
        geklommen worden, maar moet hierbij wel een valmat gebruikt worden.
        Mocht het toestel ingezet worden als speeltoestel dan mag er maximaal
        geklommen worden tot 3 meter en moet de ondergrond bestaan uit
        specifieke materialen en moet deze 300mm dik zijn. Voor nadere
        informatie rondom de wetgeving van De Boulder Mini zie de <a
          href="/pdf-bijlage/wetgevingen.pdf"
          target="_blank">bijlage</a
        >.
      </p>
